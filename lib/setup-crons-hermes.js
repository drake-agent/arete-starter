// This module generates Hermes cron job creation commands
// Called by scaffold.js when runtime === 'hermes'

export function generateCronSetup(config) {
  const { workspacePath, timezone } = config;

  const jobs = [
    {
      name: 'session-indexer',
      schedule: '0 */2 * * *',
      prompt: `Run session indexer: python3 ${workspacePath}/scripts/session_indexer.py. Report count of new sessions indexed. If 0 new, respond with NO_REPLY.`,
      script: `${workspacePath}/scripts/session_indexer.py`,
      deliver: 'local',
    },
    {
      name: 'nightly-intel',
      schedule: '0 1 * * *',
      prompt: `Read and execute the intel prompt at ${workspacePath}/drake-compass/prompts/intel.md. Input: data/normalized/*.jsonl (last 7 days). Output: write Intel section to memory/YYYY-MM-DD.md.`,
      deliver: 'local',
    },
    {
      name: 'wiki-compiler',
      schedule: '30 1 * * *',
      prompt: `Read and execute the wiki compiler prompt at ${workspacePath}/drake-compass/prompts/wiki.md. Process WIKI_INSTRUCTIONs from today's Intel report.`,
      deliver: 'local',
    },
    {
      name: 'wiki-quality-gate',
      schedule: '40 1 * * *',
      prompt: `Run wiki quality check: python3 ${workspacePath}/scripts/wiki_quality_check.py. If exit code 0: NO_REPLY. If issues found: report broken links and placeholders.`,
      script: `${workspacePath}/scripts/wiki_quality_check.py`,
      deliver: 'local',
    },
    {
      name: 'morning-direction',
      schedule: '30 8 * * *',
      prompt: `Read and execute the morning direction prompt at ${workspacePath}/drake-compass/prompts/morning-direction-v2.md. Synthesize overnight pipeline output into actionable morning brief.`,
      deliver: 'origin',
    },
    {
      name: 'nightly-distillation',
      schedule: '30 23 * * *',
      prompt: `Review today's memory log. Extract promotable items for MEMORY.md. Flag open loops. Write checkpoint.`,
      deliver: 'local',
    },
  ];

  // Generate shell script content
  let script = '#!/bin/bash\n# ARETE Hermes Cron Setup\n# Generated by arete setup\n\n';

  if (timezone) {
    script += `# Timezone: ${timezone}\n\n`;
  }

  for (const job of jobs) {
    const parts = [`hermes cron create`];
    parts.push(`--name "${job.name}"`);
    parts.push(`--schedule "${job.schedule}"`);
    parts.push(`--prompt "${job.prompt.replace(/"/g, '\\"')}"`);
    if (job.script) parts.push(`--script "${job.script}"`);
    parts.push(`--deliver "${job.deliver}"`);
    script += parts.join(' \\\n  ') + '\n\n';
  }

  return { jobs, script };
}
