// This module generates Hermes cron job creation commands
// Called by scaffold.js when runtime === 'hermes'

// POSIX shell single-quote escaping: wrap in single quotes, replace every ' with '\''
function shSingleQuote(s) {
  return `'${String(s).replace(/'/g, `'\\''`)}'`;
}

// Safe characters for workspace paths (same policy as scaffold.js)
const SAFE_WORKSPACE_RE = /^[a-zA-Z0-9._\-/\\ ~]+$/;

export function generateCronSetup(config) {
  const { workspacePath, timezone } = config;

  if (!SAFE_WORKSPACE_RE.test(String(workspacePath))) {
    throw new Error(`Unsafe characters in workspacePath: ${workspacePath}`);
  }

  // Use $WORKSPACE env var in prompts to avoid leaking absolute paths
  const ws = '$WORKSPACE';

  const jobs = [
    {
      name: 'session-indexer',
      schedule: '0 */2 * * *',
      prompt: `Run session indexer: python3 ${ws}/scripts/session_indexer.py. Report count of new sessions indexed. If 0 new, respond with NO_REPLY.`,
      script: `${workspacePath}/scripts/session_indexer.py`,
      deliver: 'local',
    },
    {
      name: 'nightly-intel',
      schedule: '0 1 * * *',
      prompt: `Read and execute the intel prompt at ${ws}/drake-compass/prompts/intel.md. Input: data/normalized/*.jsonl (last 7 days). Output: write Intel section to memory/YYYY-MM-DD.md.`,
      deliver: 'local',
    },
    {
      name: 'wiki-compiler',
      schedule: '30 1 * * *',
      prompt: `Read and execute the wiki compiler prompt at ${ws}/drake-compass/prompts/wiki.md. Process WIKI_INSTRUCTIONs from today's Intel report.`,
      deliver: 'local',
    },
    {
      name: 'wiki-quality-gate',
      schedule: '40 1 * * *',
      prompt: `Run wiki quality check: python3 ${ws}/scripts/wiki_quality_check.py. If exit code 0: NO_REPLY. If issues found: report broken links and placeholders.`,
      script: `${workspacePath}/scripts/wiki_quality_check.py`,
      deliver: 'local',
    },
    {
      name: 'morning-direction',
      schedule: '30 8 * * *',
      prompt: `Read and execute the morning direction prompt at ${ws}/drake-compass/prompts/morning-direction-v2.md. Synthesize overnight pipeline output into actionable morning brief.`,
      deliver: 'origin',
    },
    {
      name: 'nightly-distillation',
      schedule: '30 23 * * *',
      prompt: `Review today's memory log. Extract promotable items for MEMORY.md. Flag open loops. Write checkpoint.`,
      deliver: 'local',
    },
  ];

  // Generate shell script content with single-quote escaping for defense in depth
  let script = '#!/bin/bash\n# ARETE Hermes Cron Setup\n# Generated by arete setup\nset -euo pipefail\n\n';
  script += `export WORKSPACE=${shSingleQuote(workspacePath)}\n\n`;

  if (timezone) {
    script += `# Timezone: ${timezone}\n\n`;
  }

  for (const job of jobs) {
    const parts = [`hermes cron create`];
    parts.push(`--name ${shSingleQuote(job.name)}`);
    parts.push(`--schedule ${shSingleQuote(job.schedule)}`);
    parts.push(`--prompt ${shSingleQuote(job.prompt)}`);
    if (job.script) parts.push(`--script ${shSingleQuote(job.script)}`);
    parts.push(`--deliver ${shSingleQuote(job.deliver)}`);
    script += parts.join(' \\\n  ') + '\n\n';
  }

  return { jobs, script };
}
