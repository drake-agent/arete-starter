# ── 용신 도출 (Step 3) ──────────────────────────────────────────

def derive_yongshin(saju: Saju, strength: dict, gyeokguk: dict) -> dict:
    """
    용신·희신 도출.
    신강이면 설기(식상/재성/관성)가 용신.
    신약이면 부조(인성/비겁)가 용신.
    """
    일간_오행 = 천간_오행[saju.ilgan]
    is_strong = strength['strength'] == '신강'

    오행_생산 = {'木': '火', '火': '土', '土': '金', '金': '水', '水': '木'}  # 내가 생하는 것
    오행_극대상 = {'木': '土', '土': '水', '水': '火', '火': '金', '金': '木'}  # 내가 극하는 것
    오행_나를생 = {'木': '水', '火': '木', '土': '火', '金': '土', '水': '金'}  # 나를 생하는 것

    if is_strong:
        # 신강: 에너지 분출 필요
        용신_오행 = 오행_생산[일간_오행]  # 식상
        희신_오행 = 오행_극대상[일간_오행]  # 재성
        기신_오행 = 오행_나를생[일간_오행]  # 인성
        용신_역할 = '설기 (에너지 분출)'
    else:
        # 신약: 에너지 보충 필요
        용신_오행 = 오행_나를생[일간_오행]  # 인성
        희신_오행 = 일간_오행  # 비겁
        기신_오행 = 오행_극대상[일간_오행]  # 재성 (재다신약)
        용신_역할 = '부조 (에너지 보충)'

    return {
        '용신': 용신_오행,
        '희신': 희신_오행,
        '기신': 기신_오행,
        '역할': 용신_역할,
        '원리': f"{'신강→설기' if is_strong else '신약→부조'}",
    }


# ── 통합 분석 (Step 0-3) ──────────────────────────────────────

def full_analysis(saju: Saju) -> dict:
    """Step 0-3 통합 실행."""
    strength = judge_strength(saju)
    gyeokguk = determine_gyeokguk(saju)
    yongshin = derive_yongshin(saju, strength, gyeokguk)

    return {
        'saju': saju,
        'strength': strength,
        'gyeokguk': gyeokguk,
        'yongshin': yongshin,
    }
